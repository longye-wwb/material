public class SplitterLightDataDto {

    private Float waveLength;
    private Float te;
    private Float tm;

    public SplitterLightDataDto() {
    }

    public Float getWaveLength() {
        return waveLength;
    }

    public void setWaveLength(Float waveLength) {
        this.waveLength = waveLength;
    }

    public Float getTe() {
        return te;
    }

    public void setTe(Float te) {
        this.te = te;
    }

    public Float getTm() {
        return tm;
    }

    public void setTm(Float tm) {
        this.tm = tm;
    }
}
